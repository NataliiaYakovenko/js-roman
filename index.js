function factorial(number){
    let res = 1; // у цій змінній ми будемо накопичувати добуток

    if (number === 0 || number === 1){
        return 1;
    } else if (number<0){
        return null
    } 

    for(let it = 1; it <= number; it++){
        res = res * it
    }
    return res;
}
