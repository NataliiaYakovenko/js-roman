//варіант1 - Ітеративне мислення
function pow(x,n){
  let result = 1; //змінна у якій ми будемо накопичувати добуток
  //множимо result на x  n разів у циклі
for(let i = 0; i < n;i++){
  result *=x;
}
return result

}
