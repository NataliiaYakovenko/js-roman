// Increment 
let superNumber = 1;
superNumber++;
console.log(superNumber);

// Decrement
let superNumber1 = 2;
superNumber1--;
console.log(superNumber1)

// Prefix
++superNumber;
--superNumber1;
console.log(superNumber);
console.log(superNumber1)

// Postfix
superNumber++;
console.log(superNumber);
superNumber1--;
console.log(superNumber1)


