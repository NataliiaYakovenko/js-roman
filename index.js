//варіант2 - Рекурсивне мислення- спростовуємо завдання і викликаємо функцією саму себе
function powRecursive(x,n){
  if(n===1){
    return x;
  }else{
    return x* powRecursive(x,n-1); //функція повертає саму себе на ступень меньше
  }
}
